void handle_button_interrupts(void* context, alt_u32 id){
	edge_capture = IORD_ALTERA_AVALON_PIO_EDGE_CAP(BUTTON_BASE);
	IOWR_ALTERA_AVALON_PIO_EDGE_CAP(BUTTON_BASE, 0);

	alt_u32 switches_value = IORD_ALTERA_AVALON_PIO_DATA(SWITCH_BASE);

	//Choose colors from switches 10, 9, 8
	switch(switches_value & 0x380){
		case 0x000:
			count = 0;
			break;
		case 0x080:
			count = 1;
			break;
		case 0x100:
			count = 2;
			break;
		case 0x180:
			count = 3;
			break;
		case 0x200:
			count = 4;
			break;
		case 0x280:
			count = 5;
			break;
		case 0x300:
			count = 6;
			break;
		case 0x380:
			count = 7;
			break;
		default:
			count = 2;
	}

	//Button 1 => move horizontal,  X coords
	if(edge_capture == 1 && !((switches_value & 0x04) == 0x04) && DrawEnabled){
		//Move to the left
		if((switches_value & 0x01) == 0 && (x0 - 4) > 0 && (drawings[index][r][c - 1] | freeDrawing ) ){
			x0 -= 4;
			x1 -= 4;

			if (!freeDrawing)
				c--;
		//Move to the right
		}
		else if((switches_value & 0x01) == 0x01 && (x1 + 4) < 319 && ( drawings[index][r][c + 1] | freeDrawing ) ){
			x0 += 4;
			x1 += 4;

			if(!freeDrawing)
				c++;
		}

		alt_up_pixel_buffer_dma_draw_box (pixel_buf_dma_dev, x0, y0, x1, y1, colors[count], 0);
	}
	//Button 2 => move vertical, Y coords
	else if(edge_capture == 2 && !((switches_value & 0x04) == 0x04) && DrawEnabled){
		//Move Up (Down in the screen)
		if((switches_value & 0x02) == 0x00  && (y1 + 4) < 239 && (drawings[index][r + 1][c] | freeDrawing) ){
			y0 += 4;
			y1 += 4;

			if(!freeDrawing)
				r++;
		}
		//Move Down (Up in the screen)
		else if((switches_value & 0x02) == 0x02 && (y0 - 4) > 0 && (drawings[index][r - 1][c] | freeDrawing) ){
			y0 -= 4;
			y1 -= 4;

			if(!freeDrawing)
				r--;
		}

		alt_up_pixel_buffer_dma_draw_box (pixel_buf_dma_dev, x0, y0, x1, y1, colors[count], 0);
	}
	//Button 3 => move in Diagonal, (X,Y) together coords
	else if(edge_capture == 4  && ((switches_value & 0x04) == 0x04) && DrawEnabled ){
		//Move Up (Down in the screen) and to the Left
		if((switches_value & 0x03) == 0x00 && (x0 - 4) > 0 && (y1 + 4) < 239 && (drawings[index][r + 1][c - 1] | freeDrawing)){
			x0 -= 4;
			y0 += 4;
			x1 -= 4;
			y1 += 4;

			if(!freeDrawing){
				r++;
				c--;
			}
		}
		//Move Up (Down in the screen) and to the Right
		else if((switches_value & 0x03) == 0x01  && (x1 + 4) < 319 && (y1 + 4) < 239 && (drawings[index][r + 1][c + 1] | freeDrawing)){
			x0 += 4;
			y0 += 4;
			x1 += 4;
			y1 += 4;

			if(!freeDrawing){
				r++;
				c++;
			}
		}
		//Move Down (Up in the screen) and to the Left
		else if((switches_value & 0x03) == 0x02  &&(x0 - 4) > 0 && (y0 - 4) > 0 && (drawings[index][r - 1][c - 1] | freeDrawing)){
			x0 -= 4;
			y0 -= 4;
			x1 -= 4;
			y1 -= 4;

			if(!freeDrawing){
				r--;
				c--;
			}
		}
		//Move Down (Up in the screen) and to the Right
		else if((switches_value & 0x03) == 0x03 && (x1 + 4) < 319 && (y0 - 4) > 0 && (drawings[index][r - 1][c + 1] | freeDrawing)){
			x0 += 4;
			y0 -= 4;
			x1 += 4;
			y1 -= 4;

			if(!freeDrawing){
				r--;
				c++;
			}
		}

		alt_up_pixel_buffer_dma_draw_box (pixel_buf_dma_dev, x0, y0, x1, y1, colors[count], 0);
	}

	//Button 4, clear and change picture, 4 options Free drawing, draw in Pac-man, display pic1, display pic2
	else if (edge_capture == 8){
		//Clear picture if user is on Pac-man, or free drawing. Don't clear if user displays the same picture
		if((switches_value & 0x060) != previousDrawing){
			alt_up_pixel_buffer_dma_clear_screen (pixel_buf_dma_dev, 0);
			usleep (1000000);
		}

		//Free drawing
		if((switches_value & 0x060) == 0x00){
			x0 = 159, y0 = 119, x1 = 161, y1 = 121;			//Starting position
			alt_up_pixel_buffer_dma_draw_box (pixel_buf_dma_dev, x0, y0, x1, y1, colors[count], 0);
			freeDrawing = 1;	  							//Borders are set to borders of screen
			DrawEnabled = 1;	  							//User can draw boxes
			previousDrawing = -1; 							//In order to clear without restriction
		}
		//draw in Pac-Man
		else if((switches_value & 0x060) == 0x20){
			freeDrawing = 0;	  							//Borders are set to borders of the array, the '1's.
			DrawEnabled = 1;	  							//User can draw boxes
			previousDrawing = -1; 							//In order to clear without restriction
			index = 0;										//Choose Pac-Man drawing from the drawings array
		}
		//Display pic1
		else if((switches_value & 0x060) == 0x40){
			freeDrawing = 0;								//Borders are set to borders of the array, the '1's.
			DrawEnabled = 0;								//User cannot draw boxes
			index = 1;										//Choose first picture from the drawings array
		}
		//Display pic2
		else{
			freeDrawing = 0;								//Borders are set to borders of the array, the '1's.
			DrawEnabled = 0;								//User cannot draw boxes
			index = 2;										//Choose second picture from the drawings array
		}

		//Code to draw the Pac-man, pic1, and 2. If pic1 or pic2 is already drawn. Don't draw it again.
		if(freeDrawing == 0 && previousDrawing != (switches_value & 0x060)){
			if (index == 1 | index == 2){
				previousDrawing = (switches_value & 0x060); 	//Restriction on clearing pic1 and pic2
				DrawEnabled = 0;
			}
			x0 = 59, y0 = 19, x1 = 61, y1 = 21; //Starting position
			int rows = 51, columns = 51;
			int offset;
			for(int i = 0; i < rows; i++){
				offset = 0;
				for(int j = 0; j < columns; j++){
					offset += 4;
					if(drawings[index][i][j] == 0){
					alt_up_pixel_buffer_dma_draw_box (pixel_buf_dma_dev, x0 + offset, y0, x1 + offset, y1, 0xFFFF, 0);
					}
				}
					y0 += 4;
					y1 += 4;
			}
			x0 = 147, y0 = 119, x1 = 149, y1 = 121; // Where we place the user when it ends


			//set r and c to something 				//In order to track borders of Array
			if((switches_value & 0x060) == 0x20){
				r = 25;
				c = 21;
			}
		}
	}
}


